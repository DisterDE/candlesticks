package com.disterde.candlesticks.util

/**
 * Type alias for the International Securities Identification Number (ISIN).
 *
 * ISIN is a globally recognized standard for identifying financial instruments.
 * It is represented as a `String` but semantically indicates an identifier for a security.
 *
 * Usage of this alias enhances code readability and conveys intent when working with ISINs.
 */
typealias ISIN = String

/**
 * Type alias for the price of a financial instrument.
 *
 * Prices are represented as `Double`, allowing for fractional values.
 * This alias improves readability by indicating that the `Double` represents a price.
 */
typealias Price = Double